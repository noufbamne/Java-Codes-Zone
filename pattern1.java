public class pattern1 {
    public static void main(String[] args) {
        
        int n = 5;
        for (int i = 1; i <= n; i++) {
            if (i % 2 == 0) {
                System.out.print("2");
            }
            else{
                System.out.print("1");
            }
            System.out.println();
        }
    }
}
